package Academy.devonline.java.basic.section_Expressions;

public class ArithmeticExpressions {
    public static void main(String[] args) {
        {
            var a = 5;
            var b = 3;

            System.out.println(a + b);
            System.out.println(a - b);
            System.out.println(a * b);
            System.out.println(a / b);
            System.out.println(a % b);
        }
        {
            var a = 5.;
            var b = 3.;

            System.out.println(a + b);
            System.out.println(a - b);
            System.out.println(a * b);
            System.out.println(a / b);
            System.out.println(a % b);
        }
    }
}
